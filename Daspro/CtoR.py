# Definisi dan spesifikasi dari fungsi CelciusToReamur bernama CR adalah:
# CR : real --> real
#   CR(x) mengkonversi nilai celcius menjadi nilai reamur, sebuah bilangan real

# Realisasi
def CR(c):
    return((4/5)*c)
           
# Aplikasi
print(CR(50))
print(CR(30))
print(CR(90))
