# Definisi dan spesifikasi dari fungsi CelciusToFahrenheit bernama CF adalah:
# CF : real --> real
#   CF(x) mengkonversi nilai celcius menjadi nilai fahrenheit, sebuah bilangan real

# Realisasi
def CF(c):
    return((9/5)*c+32)
           
# Aplikasi
print(CF(50))
print(CF(30))
print(CF(90))
