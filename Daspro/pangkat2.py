# Definisi dan spesifikasi dari fungsi pangkat2 bernama Pang2 adalah:
# Pang2 : integer --> integer
#   Pang2(x) menghitung pangkat dua dari x, sebuah bilangan integer

# Realisasi
def Pang2(x):
    return(x*x)

# Aplikasi
print(Pang2(3))
print(Pang2(4))
print(Pang2(5))
print(Pang2(7))
print(Pang2(10))
