# Definisi dan spesifikasi dari fungsi pangkat3 bernama Pang3 adalah:
# Pang3 : integer --> integer
#   Pang3(x) menghitung pangkat tiga dari x, sebuah bilangan integer

# Realisasi
def Pang3(x):
    return(x*x*x)

# Aplikasi
print(Pang3(3))
print(Pang3(4))
print(Pang3(5))
print(Pang3(7))
print(Pang3(10))
